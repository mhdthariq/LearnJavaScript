const fruit = "banana";

switch (fruit) {
  case "banana":
    console.log("I am a banana");
    break;
  case "apple":
    console.log("I am an apple");
    break;
  case "grape":
    console.log("I am a grape");
    break;
  case "melon":
    console.log("I am a melon");
    break;
  default:
    console.log("I am not Fruit");
}
