// Description: Belajar penggunaan while loop di JavaScript
// Run: node whileLoop.js
// Documentation: https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/while
let i = 0;

while (i < 5) {
  console.log(`Angka ke-${i} adalah ${i}`);
  i++;
}

// do while loop
let j = 0;

do {
  console.log(`Angka ke-${j} adalah ${j}`);
  j++;
} while (j < 5);
